public class BitWiseOperators{
    public static void main(String[] args) {
        System.out.println("5 & 6 : "+ (5&6));
        System.out.println("5 | 6 : "+ (5|6));
        System.out.println("5 ^ 6 : "+ (5^6));
        System.out.println("~5 : "+ (~5));//-6
        System.out.println("5 << 2: "+ (5<<2));
        System.out.println("6 >> 1: "+ (6>>1));
    }
}